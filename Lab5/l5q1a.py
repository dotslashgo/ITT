num1=int(input('Enter the first number: '))
num2=int(input('Enter the second number: '))
op=input('Enter the operation')

if op=='+':
    print('Result= ', num1+num2)
elif op=='-':
    print('Result= ', num1-num2)
elif op=='*':
    print('Result= ', num1*num2)
elif op=='/':
    print('Result= ', num1/num2)
elif op=='%':
    print('Result= ', num1%num2)
